-- 2. TABELA DE SOLUÇÕES (CORREÇÃO BIGINT)
-- ==============================================================================

-- Cria a tabela garantindo que ID seja BIGINT para suportar Date.now()
CREATE TABLE IF NOT EXISTS public.solutions (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY, -- BIGINT é essencial aqui
    solucao TEXT NOT NULL,
    promessa TEXT,
    descricao TEXT,
    categoria TEXT,
    subcategoria TEXT,
    duracao TEXT,
    maturidade TEXT,
    fee_mensal TEXT,
    valor_base_num NUMERIC DEFAULT 0,
    variaveis_opcionais JSONB DEFAULT '[]'::jsonb,
    dica_venda TEXT,
    link TEXT,
    entregaveis TEXT[],
    diferenciais TEXT[],
    publico_alvo TEXT,
    resultado_esperado TEXT,
    is_favorite BOOLEAN DEFAULT false,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

ALTER TABLE public.solutions ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Enable read access for all users" ON public.solutions;
CREATE POLICY "Enable read access for all users" ON public.solutions FOR SELECT USING (true);

DROP POLICY IF EXISTS "Enable all access for authenticated users" ON public.solutions;
CREATE POLICY "Enable all access for authenticated users" ON public.solutions FOR ALL USING (auth.role() = 'authenticated');


